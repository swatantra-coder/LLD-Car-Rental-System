public class User {
    int userId;
    String name;
    String driverLicense;
    String phoneNumber;
    String email;

    public User(int userId, String name, String driverLicense, String phoneNumber, String email) {
        this.userId = userId;
        this.name = name;
        this.driverLicense = driverLicense;
        this.phoneNumber = phoneNumber;
        this.email = email;
    }
}
